package week4_MergeSort_QuickSort;

import java.util.Arrays;

public class Prepare {
	public static void main(String[] args) {
		int i= 0;
		int j= i+++3;
		System.out.println(i+"\t"+j);
	}
	
	//the difference between i++ and ++i
	
	/*
	give i=0
	i++: increase the value of i and return the previous value
			example: j=(i++)+3 (then j=3, i=1);
	++i: increase the value of i and return that value
			example: j=(++i)+3 (then j=4, i=1);
	*/
}
